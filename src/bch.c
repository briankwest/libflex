int flex_bch_correct(uint32_t *codeword)
{
	uint32_t cw = *codeword;
	uint32_t syn = flex_bch_syndrome(cw);
	int par_ok = flex_parity_check(cw);

	if (syn == 0 && par_ok)
		return 0;  /* no error */

	/* compute lookup key: (syndrome << 1) | parity_bad */
	uint32_t key = (syn << 1) | (par_ok ? 0 : 1);

	for (int i = 0; i < TABLE_SIZE; i++) {
		if (syndrome_table[i].key == key) {
			*codeword = cw ^ syndrome_table[i].mask;
			return 0;
		}
	}

	return -1;  /* uncorrectable (3+ bit error) */
}
