#include "libflex/bch.h"
#include "libflex/types.h"

#define BCH_GEN FLEX_BCH_POLY

uint32_t flex_bch_encode(uint32_t data21)
{
	uint32_t reg = data21 << 10;

	for (int i = 30; i >= 10; i--) {
		if (reg & (1u << i))
			reg ^= (BCH_GEN << (i - 10));
	}

	return reg & 0x3FFu;
}

uint32_t flex_codeword_build(uint32_t data21)
{
	uint32_t bch = flex_bch_encode(data21);
	uint32_t cw = (data21 << 11) | (bch << 1);

	/* even parity over bits 31..1 */
	uint32_t p = cw >> 1;
	p ^= p >> 16;
	p ^= p >> 8;
	p ^= p >> 4;
	p ^= p >> 2;
	p ^= p >> 1;
	cw |= (p & 1);

	return cw;
}

uint32_t flex_bch_syndrome(uint32_t codeword)
{
	uint32_t reg = codeword >> 1;

	for (int i = 30; i >= 10; i--) {
		if (reg & (1u << i))
			reg ^= (BCH_GEN << (i - 10));
	}

	return reg & 0x3FFu;
}

int flex_parity_check(uint32_t codeword)
{
	uint32_t p = codeword;
	p ^= p >> 16;
	p ^= p >> 8;
	p ^= p >> 4;
	p ^= p >> 2;
	p ^= p >> 1;
	return !(p & 1);
}

